package ex1;

/**
 * Represente une methode de calcul addition et soustration
 * @author diginamic
 *
 */
public class Calcul {

	/**
	 * @param a
	 * @param b
	 * @return int a+b
	 */
	public int calculAddition(int a, int b){
		return a+b;
	}
	
	/**
	 * @param a
	 * @param b
	 * @return int a-b
	 */
	public int calculSoustration(int a, int b){
		return a-b;
	}
}
